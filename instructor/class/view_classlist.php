<script>
$(document).ready(function(){
    $('.classes').click(function() {
        var page = $(this).attr('data-page');
        $.ajax({
            type: 'GET',
            url: '../instructor/navInstructor.json',
            dataType: 'html',
        }).done(function(response) {
            var data = JSON.parse(response)
            switch(page) {
                case 'viewdetails':
                    $('#mainContent').load(data[0].class["vclass"]);
                    break;
                default:
                    $('#mainContent').load(data[0].class["vclasslist"]);
            }
            $('#setSubPage').text("");
        });
    });
});
</script>